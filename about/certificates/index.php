  <script type="text/javascript">
    BX.ready(() => {
      // Popup Slider
      let popupPreviewSliders = document.querySelectorAll('.js-get-popup-slider'),
        popupContentSliders = document.querySelectorAll('.js-popup-slider'),
        popupSliderCounter = document.querySelectorAll('.popup-slider-counter span'),
        slideNext = document.querySelectorAll('.popup-slide-next'),
        slidePrev = document.querySelectorAll('.popup-slide-prev'),
        sliderActiveStart = 0,
        sliderActiveLengthStart = 0,
        slidePositionStart = 0;


      // Popup Slider - Set Actually Slide Counter
      let getSlideCount = () => {
        if (popupSliderCounter !== null) {
          popupSliderCounter[sliderActiveStart].innerHTML = (slidePositionStart + 1) + '/' + sliderActiveLengthStart;
        }
      };

      let getNextPrev = () => {
        slideNext.forEach(itemBtn => {
          itemBtn.addEventListener('click', () => {
            let slideNow = popupContentSliders[sliderActiveStart].querySelectorAll('li');

            if (slidePositionStart !== (sliderActiveLengthStart - 1)) {
              slideNow[slidePositionStart].classList.remove('popup-slide-active');
              slideNow[slidePositionStart + 1].classList.add('popup-slide-active');

              slidePositionStart++;

              getSlideCount();
            }
          });
        });

        slidePrev.forEach(itemBtn => {
          itemBtn.addEventListener('click', () => {
            let slideNow = popupContentSliders[sliderActiveStart].querySelectorAll('li');

            if (slidePositionStart !== 0) {
              slideNow[slidePositionStart].classList.remove('popup-slide-active');
              slideNow[slidePositionStart - 1].classList.add('popup-slide-active');

              slidePositionStart--;

              getSlideCount();
            }
          });
        });
      };

      getNextPrev();

      // Popup Slider - Get Popup Content Slider
      let getPopupSlider = (targetSliderIndex, targetSliderItemIndex, targetSliderLength) => {
        if (popupContentSliders !== null) {
          popupContentSliders.forEach((popupSlidersItem, popupSlidersIndex) => {
            let popupSliderCounter = popupSlidersItem.querySelector('.popup-slider-counter span'),
              popupSlides = popupSlidersItem.querySelectorAll('li');

            // Set Actually Slide Counter
            if (popupSliderCounter !== null && popupSlidersIndex === targetSliderIndex) {
              popupSliderCounter.innerHTML = (targetSliderItemIndex + 1) + '/' + targetSliderLength;
            }

            // Check Slider
            if (popupSlidersIndex === targetSliderIndex) {
              popupSlidersItem.classList.add('popup-active');
            }

            if (popupSlides !== null) {
              popupSlides.forEach((popupSlideItem, popupSlideIndex) => {
                // Check Slide
                if (popupSlideItem.classList.contains('popup-slide-active') === true && popupSlidersIndex === targetSliderIndex) {
                  popupSlideItem.classList.remove('popup-slide-active');
                }

                if (targetSliderItemIndex === popupSlideIndex && popupSlidersIndex === targetSliderIndex) {
                  popupSlideItem.classList.add('popup-slide-active');
                }
              });
            }
          });
        }
      };

      // Popup Slider - Slider Preview Elements
      if (popupPreviewSliders !== null) {
        popupPreviewSliders.forEach((sliderItem, sliderIndex) => {

          let slides = sliderItem.querySelectorAll('li');

          if (slides !== null) {
            slides.forEach((slideItem, slideIndex, slideLength) => {

              // Get Popup
              slideItem.addEventListener('click', () => {
                getPopupSlider(sliderIndex, slideIndex, slideLength.length);

                sliderActiveStart = sliderIndex;
                slidePositionStart = slideIndex;
                sliderActiveLengthStart = slideLength.length;
              });
            });
          }
        });
      }
    });
  </script>

